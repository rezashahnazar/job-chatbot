import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();

async function main() {
  // Create Product Marketing Manager position
  await prisma.job.create({
    data: {
      title: "Product Marketing Manager",
      description: `ما به دنبال یک مدیر بازاریابی محصول با تجربه هستیم که بتواند استراتژی‌های بازاریابی محصولات ما را توسعه و اجرا کند.

مسئولیت‌ها:
- تدوین و اجرای استراتژی‌های بازاریابی محصول
- تحقیق و تحلیل بازار و رقبا
- همکاری با تیم‌های محصول، فروش و بازاریابی
- تولید محتوای بازاریابی و مستندات محصول
- مدیریت کمپین‌های بازاریابی محصول

مزایا:
- حقوق رقابتی
- بیمه درمانی
- مزایای رفاهی
- محیط کاری پویا و حرفه‌ای`,
      requirements: `شرایط احراز:
- حداقل 5 سال تجربه در بازاریابی محصول
- تسلط به اصول بازاریابی دیجیتال
- مهارت‌های تحلیلی قوی
- توانایی کار با داده و تحلیل متریک‌ها
- مهارت‌های ارتباطی و رهبری عالی
- تسلط به زبان انگلیسی
- آشنایی با ابزارهای تحلیل بازار و CRM`,
    },
  });

  // Create Product Marketing Full-Stack Developer position
  await prisma.job.create({
    data: {
      title: "Product Marketing Full-Stack Developer",
      description: `ما به دنبال یک توسعه‌دهنده فول‌استک با تمرکز بر بازاریابی محصول هستیم که بتواند راهکارهای فنی برای نیازهای بازاریابی محصول ما توسعه دهد.

مسئولیت‌ها:
- توسعه و نگهداری پلتفرم‌های بازاریابی محصول
- پیاده‌سازی سیستم‌های تحلیل داده و گزارش‌گیری
- توسعه API‌ها و میکروسرویس‌ها
- بهینه‌سازی عملکرد و مقیاس‌پذیری سیستم‌ها
- همکاری با تیم بازاریابی محصول

مزایا:
- حقوق رقابتی
- بیمه درمانی
- مزایای رفاهی
- محیط کاری پویا و حرفه‌ای`,
      requirements: `شرایط احراز:
- حداقل 3 سال تجربه در توسعه فول‌استک
- تسلط به React.js، Node.js و TypeScript
- تجربه کار با پایگاه‌های داده SQL و NoSQL
- آشنایی با معماری میکروسرویس
- تجربه کار با ابزارهای تحلیل داده
- درک قوی از اصول بازاریابی دیجیتال
- مهارت‌های حل مسئله و تفکر تحلیلی
- توانایی کار تیمی و ارتباط موثر`,
    },
  });
}

main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
